        public void populate()
        {
        DefaultTableModel dtm=(DefaultTableModel)displaytable.getModel();
        dtm.setRowCount(0);
        for(Traveloffice o: tod.getTravelofficedirectory())
        {
            Object[]row=new Object[dtm.getColumnCount()];
            row[0]=o;
            row[1]=o.getOfficeName();
            row[3]=o.getSetuptime();
            row[2]=o.getOfficeLocation();
            dtm.addRow(row);
            
        }
        
        
        }
