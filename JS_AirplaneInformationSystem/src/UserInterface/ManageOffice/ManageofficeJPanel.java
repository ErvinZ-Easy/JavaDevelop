    private void viewbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewbtnActionPerformed
        // TODO add your handling code here:
        int selectedRow= displaytable.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(null, "please select a row from table first","warning",JOptionPane.WARNING_MESSAGE);

        }
        else
        {

            to=(Traveloffice) displaytable.getValueAt(selectedRow, 0);
            numbertxt.setText(to.getOfficeNum());
            nametxt.setText(to.getOfficeName());
            locationtxt.setText(to.getOfficeLocation());
            timetxt.setText(String.valueOf(to.getSetuptime()));
            numbertxt.setEnabled(false);
            nametxt.setEnabled(false);
            locationtxt.setEnabled(false);
            timetxt.setEnabled(false);
            
            
        }
    }//GEN-LAST:event_viewbtnActionPerformed

    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = displaytable.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Traveloffice too = (Traveloffice)displaytable.getValueAt(selectedRow, 0);

                tod.deletetraveloffice(too);
                populate();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_deletebtnActionPerformed

