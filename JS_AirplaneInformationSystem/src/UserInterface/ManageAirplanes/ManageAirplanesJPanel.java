    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblairplane.getSelectedRow();
        if(selectedRow >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would you like to delete the airplane","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Airplane airplane = (Airplane) tblairplane.getValueAt(selectedRow, 0);
                airplanefleet.deleteAirplane(airplane);
                populateAirplane();
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row from the table","Warning",JOptionPane.WARNING_MESSAGE);
        }
     
    }                     
    }//GEN-LAST:event_btnDeleteActionPerformed


