     public void populateAirplane(){
        DefaultTableModel dtm = (DefaultTableModel) tblairplane.getModel();
        dtm.setRowCount(0);
        for(Airplane airplane: airplanefleet.getAirplanefleet()){
            Object[] row = new Object[3];
            row[0] = airplane;
            row[1] = airplane.getPlaneNum();
            row[2] = airplane.getAirplaneYear();
            
            dtm.addRow(row);
            
        }
    }
     
     public void  SearchplaneNum(){
        DefaultTableModel dtm=(DefaultTableModel)tblairplane.getModel();
        dtm.setRowCount(0);
        String planeNum=txtsearchPlane.getText();
        for (Airplane a:airplanefleet.getPlaneNum(planeNum)){
            Object row[]= new Object[3];
            row[0]=a;
            row[1]=a.getPlaneNum();
            row[2]=a.getAirplaneYear();
            
            
            dtm.addRow(row);
        }
        
     }
