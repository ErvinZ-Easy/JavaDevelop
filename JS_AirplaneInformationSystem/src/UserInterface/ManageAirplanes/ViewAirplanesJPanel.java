    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        txtplaneNum.setEditable(false);
        txtplaneYear.setEditable(false);
        txtAirliner.setEditable(false);
        JCBavail.setEnabled(false);
        btnUpdate.setEnabled(true);
         
         
         airplane.setPlaneNum(txtplaneNum.getText());
         int year=Integer.parseInt(txtplaneYear.getText());
         airplane.setAirplaneYear(year);
         
         airplane.setAirliner(txtAirliner.getText());
         airplane.setPlaneStatus(String.valueOf(JCBavail.getSelectedItem()));
         btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
        
         JOptionPane.showMessageDialog(null,"Airplane Information updated successfully");
    }//GEN-LAST:event_btnSaveActionPerformed


