    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
         int selectedRow = airlinerjtable.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Airliner l = (Airliner)airlinerjtable.getValueAt(selectedRow, 0);

                airlinerdirectory.deleteAirliner(l);
                populate();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
        
    }//GEN-LAST:event_deletebtnActionPerformed

    private void viewbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewbtnActionPerformed
        // TODO add your handling code here:
          int selectedRow= airlinerjtable.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(null, "please select a row from table first","warning",JOptionPane.WARNING_MESSAGE);

        }
        else
        {

            airliner=(Airliner) airlinerjtable.getValueAt(selectedRow, 0);
            passwordtxt.setText(airliner.getPassword());
            nametxt.setText(airliner.toString());
            
            passwordtxt.setEnabled(false);
            nametxt.setEnabled(false);
            
            
        }
    }//GEN-LAST:event_viewbtnActionPerformed
 
