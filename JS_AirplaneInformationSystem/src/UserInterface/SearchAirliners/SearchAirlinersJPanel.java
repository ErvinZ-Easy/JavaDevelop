    public void populate()
    {
        DefaultTableModel dtm=(DefaultTableModel)airlinerjtable.getModel();
        dtm.setRowCount(0);
        for(User a: airlinerdirectory.getAirlinerdirectory())
        {
            Object[]row=new Object[dtm.getColumnCount()];
            row[0]=a;
            row[1]=a.getPassword();
            dtm.addRow(row);
            
        }
    }
