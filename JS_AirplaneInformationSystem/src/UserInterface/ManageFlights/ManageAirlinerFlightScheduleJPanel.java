    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here: 
         int selectedRow = tblflight.getSelectedRow();
         if(selectedRow<0){
             JOptionPane.showMessageDialog(null,"Please select a row from table to view details","Warning",JOptionPane.WARNING_MESSAGE);
         }
         else{
             Flight flight = (Flight)tblflight.getValueAt(selectedRow,0);
                ViewFlightsJPanel vfjp=new ViewFlightsJPanel(CardSequenceJPanel,flight);
                CardSequenceJPanel.add("ViewFlightsJPanel",vfjp);
                CardLayout layout= (CardLayout)CardSequenceJPanel.getLayout();
               layout.next(CardSequenceJPanel);
         }        
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblflight.getSelectedRow();
        if(selectedRow >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would you like to cancel the flight","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Flight flight = (Flight) tblflight.getValueAt(selectedRow, 0);
                flightschedule.cancelFlight(flight);
                populateTable();
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row from the table","Warning",JOptionPane.WARNING_MESSAGE);
        }
     
    }                     
    }//GEN-LAST:event_btnCancelActionPerformed


