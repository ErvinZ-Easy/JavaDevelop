     public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblflight.getModel();
        dtm.setRowCount(0);
        for(Flight flight: flightschedule.getFlightschedule()){
            Object[] row = new Object[6];
            row[0] = flight;
            row[1] = flight.getFlightNum();
            row[2] = flight.getAirplaneNum();
            row[3] = flight.getDepartLoc();
            row[4] = flight.getArrivalLoc();
            row[5] = flight.getTripType();
            
            dtm.addRow(row);
            
        }
    }

