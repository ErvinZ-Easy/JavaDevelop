    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
         String fliNum=txtfliNum.getText();
             if(fliNum==null || fliNum.equals("")){
                 JOptionPane.showMessageDialog(null,"The Flight Number cannot be empty");
                 return;
             }
         String planeNum=txtplaneNum.getText();
             if(planeNum==null || planeNum.equals("")){
                 JOptionPane.showMessageDialog(null,"The Airplane Number cannot be empty");
                 return;
             }
        String arrloc=txtarrLoc.getText();
             if(arrloc==null || arrloc.equals("")){
                 JOptionPane.showMessageDialog(null,"The Arrival Location cannot be empty");
                 return;
             }
             
        String deptloc=txtdepLoc.getText();
             if(deptloc==null || deptloc.equals("")){
                 JOptionPane.showMessageDialog(null,"The Depart Location cannot be empty");
                 return;
             }
         String deptdate=txtdeptDate.getText();
             if(deptdate==null || deptdate.equals("")){
                 JOptionPane.showMessageDialog(null,"The Depart Date cannot be empty");
                 return;
             }
             String arrdate=txtarrDate.getText();
             if(arrdate==null || arrdate.equals("")){
                 JOptionPane.showMessageDialog(null,"The Arrival Date cannot be empty");
                 return;
             }
             
             String airliner=txtairliner.getText();
             if(airliner==null || airliner.equals("")){
                 JOptionPane.showMessageDialog(null,"The Airliner cannot be empty");
                 return;
             }
             
        Flight flight = flightschedule.addFlight();
        flight.setFlightNum(txtfliNum.getText());
        flight.setAirplaneNum(txtplaneNum.getText());
        flight.setArrivalLoc(txtarrLoc.getText());
        flight.setDepartLoc(txtdepLoc.getText());
        flight.setTimePeriod(String.valueOf(cbtimePeriod.getSelectedItem()));
        flight.setTripType(String.valueOf(JCBtrip.getSelectedItem()));
        flight.setArrivalDate(txtarrDate.getText());
        flight.setReturnDate(txtreturnDate.getText());
        flight.setDeptDate(txtdeptDate.getText());
        flight.setAirliner(txtairliner.getText());
        flight.setStatus(String.valueOf(JCBstatus.getSelectedItem()));
        

        JOptionPane.showMessageDialog(null,"Flight created successfully");
    }//GEN-LAST:event_btnCreateActionPerformed

