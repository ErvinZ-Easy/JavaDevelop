    private void ServiceJCBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ServiceJCBActionPerformed
        // TODO add your handling code here:
        if(ServiceJCB.getSelectedItem().toString().equals("Service")){
            workJPanel.removeAll();
        }else if(ServiceJCB.getSelectedItem().toString().equals("RoomInfo")){
            ResidenceJPanel rjp=new ResidenceJPanel(workJPanel);
            workJPanel.add(rjp);
            CardLayout ly=(CardLayout)workJPanel.getLayout();
            ly.next(workJPanel);
        }else if(ServiceJCB.getSelectedItem().toString().equals("Meal")){
            MTJPanel mjp=new MTJPanel();
            workJPanel.add(mjp);
            CardLayout ly=(CardLayout)workJPanel.getLayout();
            ly.next(workJPanel);
        }else if(ServiceJCB.getSelectedItem().toString().equals("Travel")){
            MTJPanel mjp=new MTJPanel();
            workJPanel.add(mjp);
            CardLayout ly=(CardLayout)workJPanel.getLayout();
            ly.next(workJPanel);
        }
    }//GEN-LAST:event_ServiceJCBActionPerformed

