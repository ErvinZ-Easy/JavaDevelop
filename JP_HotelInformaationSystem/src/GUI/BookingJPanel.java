    public void callSql(String sql){
        
        try{
            ResultSet rs=DBOperate.select(sql);
            statusJL.setText("Status: select successfully");
            int col = rs.getMetaData().getColumnCount();
            while (rs.next()) {
                for (int i = 1; i <= col; i++) {
                    viewJTA.setText(viewJTA.getText()+"     "+rs.getString(i)+" ");

                }
                viewJTA.setText(viewJTA.getText()+"\n");
            }
        }catch(Exception e){
            e.printStackTrace();
         }
    }
    
    public int callUpdate(String sql){
        try{
            int x=DBOperate.update(sql);
            if(x>0){
                return 1;
            }else{
                return 0;
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        return 0;
    }
    
    public void populateJCB(){
        RTJCB.removeAllItems();
        String sql="select RoomType from RoomType";
        try{
            ResultSet rs=DBOperate.select(sql);
            //statusJL.setText("Status: select successfully");
            int col = rs.getMetaData().getColumnCount();
            while (rs.next()) {
                for (int i = 1; i <= col; i++) {
                    RTJCB.addItem(rs.getString(i));
                    
                }
                
            }
        }catch(Exception e){
            e.printStackTrace();
         }
    }
    
