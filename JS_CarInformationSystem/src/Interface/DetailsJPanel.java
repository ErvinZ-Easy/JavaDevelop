    public void popTable(){
        YesJCB.setSelected( car.isAvailCar()  );
        MTF.setText(car.getManufacturer());
        MYTF.setText(  String.valueOf(car.getManufactureYear())  );
        STF.setText(  String.valueOf(car.getSeats())  );
        ExpiredJCB.setSelected(  !car.isMaintenanceCertificate() );
        MNTF.setText(  String.valueOf(car.getModelNo())  );
        CTF.setText(car.getCity());
        CUTTF.setText(car.getCatalogTime());
        SNTF.setText(String.valueOf(car.getSerialNo()) );
    }
    
    private void btnBACKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBACKActionPerformed
        // TODO add your handling code here:
        OperateJPanel.remove(this);
        CardLayout layout=(CardLayout) OperateJPanel.getLayout();
        Component[] cs=OperateJPanel.getComponents();
        for(Component c:cs){
         if(c instanceof SearchCarJPanel){
            SearchCarJPanel sjp=(SearchCarJPanel) c;
            sjp.popTable(carcatalog.getCarCatalog(),Integer.parseInt(SNTF.getText()) );
         }
        }
        layout.previous(OperateJPanel);
    }//GEN-LAST:event_btnBACKActionPerformed

