    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        int serialNo=Integer.parseInt(SNTF.getText());
        for(Car c: carcatalog.getSerialNo(serialNo))
        {
            YesJCB.setSelected( c.isAvailCar()  );
            MTF.setText(c.getManufacturer());
            MYTF.setText(  String.valueOf(c.getManufactureYear())  );
            STF.setText(  String.valueOf(c.getSeats())  );
            ExpiredJCB.setSelected(  !c.isMaintenanceCertificate() );
            MNTF.setText(  String.valueOf(c.getModelNo())  );
            CTF.setText(c.getCity());
            CUTTF.setText(c.getCatalogTime());
            JOptionPane.showMessageDialog(null,"Found Successfully!");
            btnUpdate.setEnabled(true);
            break;
        }

    }//GEN-LAST:event_btnSearchActionPerformed

