    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int serialNo=Integer.parseInt(SNTF.getText());
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String time=df.format(new Date());
        for(Car c: carcatalog.getSerialNo(serialNo))
        {

            c.setAvailCar(  YesJCB.isSelected()  );
            c.setManufacturer(MTF.getText());
            c.setManufactureYear( Integer.parseInt(MYTF.getText())  );
            c.setSeats( Integer.parseInt(  STF.getText() )  );
            c.setMaintenanceCertificate( !ExpiredJCB.isSelected());
            c.setModelNo( Integer.parseInt(MNTF.getText()) );
            c.setCity( CTF.getText() );
            c.setCatalogTime(time);
            CUTTF.setText(time);

        }
        JOptionPane.showMessageDialog(null,"Update Successfully!");
    }//GEN-LAST:event_btnUpdateActionPerformed

