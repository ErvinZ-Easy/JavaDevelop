    public String getUATime(){
        ArrayList<String> catalogTime=new ArrayList<>();
        for(Car c:carcatalog.getCarCatalog()){
           
           catalogTime.add( c.getCatalogTime() );
           
        }
        Collections.sort(catalogTime);
        Collections.reverse(catalogTime);
        String time=catalogTime.get(0);
        return time;
    }
    
    public String getPTime(){
        ArrayList<String> list=new ArrayList<>();
        for(int i=1;i<=3;i++)
        {
            String path="src\\Properties\\car"+i+".properties";
            File file = new File(path);
            Long lastModified = file.lastModified();
            Date date = new Date(lastModified);
            String x =String.valueOf(date);
            list.add(x);
        }
        Collections.sort(list);
        Collections.reverse(list);
        String time=list.get(0);
        return time;
    }    
    
