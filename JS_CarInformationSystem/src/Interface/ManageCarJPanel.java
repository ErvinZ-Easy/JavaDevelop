    public void getProperties() throws FileNotFoundException, IOException{
    
        for(int i=1;i<=3;i++)
        {
            Properties props = new Properties();
            String filePath="src\\Properties\\car"+i+".properties";
            InputStream in = new BufferedInputStream(new FileInputStream(filePath));  
            props.load(in);                 

            Car car=carcatalog.addCar(); 
            car.setAvailCar(Boolean.parseBoolean(props.getProperty("availCar")) );
            car.setManufacturer(props.getProperty("manufacturer"));
            car.setManufactureYear(Integer.parseInt(props.getProperty("manufactureYear")) );
            car.setSeats(Integer.parseInt(props.getProperty("seats")) );
            car.setSerialNo(Integer.parseInt(props.getProperty("serialNo")) );
            car.setModelNo(Integer.parseInt(props.getProperty("modelNo")));
            car.setCatalogTime(props.getProperty("catalogTime"));
            car.setCity(props.getProperty("city"));
            car.setMaintenanceCertificate(Boolean.parseBoolean(props.getProperty("maintenanceCertificate")) );
        }
        JOptionPane.showMessageDialog(null,"Cars Successfully created!");
        popTable();
    }
    
    public void popTable(){
        DefaultTableModel dtm=(DefaultTableModel)ViewJTable.getModel();
        dtm.setRowCount(0);       
        for(Car c: carcatalog.getCarCatalog())
        {
            Object row[]=new Object[7];
            row[0]=c;
            row[1]=c.getSerialNo();
            row[2]=c.getModelNo();
            row[3]=c.getManufacturer();
            row[4]=c.getSeats();
            row[5]=c.isMaintenanceCertificate();
            row[6]=c.isAvailCar();
            dtm.addRow(row);
            
        }
    }
    
    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        boolean ac;
        ac = YJCheckBox.isSelected();
        String ct=CatalogTimeTF.getText();
        String c=CityTF.getText();
        boolean mc;
        mc=YJ2CheckBox.isSelected();
        String m=MTF.getText();
        int mn=Integer.parseInt(ModelNoTF.getText());
        int s=Integer.parseInt(SeatsTF.getText());
        int sn=Integer.parseInt(SerialNoTF.getText());
        int my=Integer.parseInt(MakeYearTF.getText());

        Car car=carcatalog.addCar();
        car.setAvailCar(ac);
        car.setCatalogTime(ct);
        car.setCity(c);
        car.setMaintenanceCertificate(mc);
        car.setManufacturer(m);
        car.setModelNo(mn);
        car.setSeats(s);
        car.setSerialNo(sn);
        car.setManufactureYear(my);

        JOptionPane.showMessageDialog(null,"Car Successfully created!");
        CityTF.setText("");
        SerialNoTF.setText("");
        ModelNoTF.setText("");
        SeatsTF.setText("");
        MTF.setText("");
        MakeYearTF.setText("");
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        CatalogTimeTF.setText(df.format(new Date()));
        YJCheckBox.setSelected(false);
        YJ2CheckBox.setSelected(false);
        popTable();
    }//GEN-LAST:event_btnCreateActionPerformed

