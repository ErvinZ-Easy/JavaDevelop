    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        int selectedRow=ViewJTable.getSelectedRow();
        if(selectedRow>=0){
            Car car=(Car)ViewJTable.getValueAt(selectedRow, 0);
            car.setAvailCar(YJCheckBox.isSelected());
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            CatalogTimeTF.setText(df.format(new Date()));
            car.setCatalogTime(df.format(new Date()));
            car.setCity(CityTF.getText());
            car.setMaintenanceCertificate(YJ2CheckBox.isSelected());
            car.setManufacturer(MTF.getText());
            car.setModelNo(Integer.parseInt(ModelNoTF.getText()) );
            car.setSeats(Integer.parseInt(SeatsTF.getText()) );
            car.setSerialNo(Integer.parseInt(SerialNoTF.getText()) );
            car.setManufactureYear(Integer.parseInt(MakeYearTF.getText()) );
            JOptionPane.showMessageDialog(null,"update successfully");
            popTable();
            btnUpdate.setEnabled(true);
            btnCreate.setEnabled(true);
            btnProperties.setEnabled(true);
            btnConfirm.setEnabled(false);
        }else{
        JOptionPane.showMessageDialog(null,"select row");
        }
    }//GEN-LAST:event_btnConfirmActionPerformed


