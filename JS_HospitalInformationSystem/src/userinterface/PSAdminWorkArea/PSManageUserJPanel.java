  public void populateTable(Organization org){
      
        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();
        model.setRowCount(0);
        for (UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {
            Object row[] = new Object[2];
            row[0] = ua;
            row[1] = ua.getRole();
            ((DefaultTableModel) userJTable.getModel()).addRow(row);
        }
        
    }
  
    private void JcbOrgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JcbOrgActionPerformed
      Organization org=(Organization)JcbOrg.getSelectedItem();
        if(org!=null){
            for(People p:org.getPeopleDirectory().getPeopleList()){
                JcbPer.removeAllItems();
                JcbPer.addItem(p);
            }
            for(Role role:org.getSupportedRole()){
                JcbRole.removeAllItems();
                JcbRole.addItem(role);
            }
            populateTable(org);
        }
    }//GEN-LAST:event_JcbOrgActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
       String userName = txtUsername.getText();
        String password =txtPassword.getText();
        Organization organization = (Organization) JcbOrg.getSelectedItem();
        People people= (People) JcbPer.getSelectedItem();
        Role role = (Role) JcbRole.getSelectedItem();

        organization.getUserAccountDirectory().createUserAccount(userName, password, people, role);
        populateTable(organization);     
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnBackActionPerformed

   
    
    public void populateComboBox(){
        JcbOrg.removeAllItems();
        JcbPer.removeAllItems();
        JcbRole.removeAllItems();
        for(Organization o:enterprise.getOrganizationDirectory().getOrganizationList()){
            if(o.getOrganizationType().getValue().equals("SalesDept")||o.getOrganizationType().getValue().equals("Warehouse")){
                JcbOrg.addItem(o);
            }
        }

    }

