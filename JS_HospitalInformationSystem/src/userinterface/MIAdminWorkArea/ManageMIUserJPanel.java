    private void delJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delJBActionPerformed
        // TODO add your handling code here:
        int selectedrow=userJTable.getSelectedRow();
        Organization org=(Organization)OrgJCB.getSelectedItem();
        if(selectedrow>=0){
            UserAccount ua =(UserAccount)userJTable.getValueAt(selectedrow,0);
            org.getUserAccountDirectory().deleteUserAccount(ua);
            for(int i=0; i<org.getRtd().getReservationtimeSet().size();i++)
            //  reservationtime rt: org.getRtd().getReservationtimeSet())
        {
            org.getRtd().getReservationtimeSet().clear();
            //org.getRtd().deletereservationtime(rt);
        }
        JOptionPane.showMessageDialog(null,"delete successfully");
        nameJTextField.setText("");
        passwordJTextField.setText("");
        populateTable(org);
        }
    }//GEN-LAST:event_delJBActionPerformed


