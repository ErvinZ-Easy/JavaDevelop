    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        String userName = nameJTextField.getText();
        String password = passwordJTextField.getText();
        Organization organization = (Organization) OrgJCB.getSelectedItem();
        People people= (People) perJCB.getSelectedItem();
        Role role = (Role) roleJCB.getSelectedItem();
        organization.getUserAccountDirectory().createUserAccount(userName, password, people, role);
        populateTable(organization);
    }//GEN-LAST:event_createUserJButtonActionPerformed

    public void populateTable(Organization org){
        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();
        model.setRowCount(0);
        for (UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {
            Object row[] = new Object[2];
            row[0] = ua;
            row[1] = ua.getRole();
            ((DefaultTableModel) userJTable.getModel()).addRow(row);
        }
        
    }
     public void populateComboBox(){
        OrgJCB.removeAllItems();
        perJCB.removeAllItems();
        roleJCB.removeAllItems();
        for(Organization o:enterprise.getOrganizationDirectory().getOrganizationList()){
            if(o.getOrganizationType().getValue().equals("SettlementDept")||o.getOrganizationType().getValue().equals("PatientDept")){
                OrgJCB.addItem(o);
            }
        }

    }
    private void OrgJCBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OrgJCBActionPerformed
        Organization org=(Organization)OrgJCB.getSelectedItem();
        if(org!=null){
            for(People p:org.getPeopleDirectory().getPeopleList()){
                perJCB.removeAllItems();
                perJCB.addItem(p);
            }
            for(Role role:org.getSupportedRole()){
                roleJCB.removeAllItems();
                roleJCB.addItem(role);
            }
            populateTable(org);
        }
    }//GEN-LAST:event_OrgJCBActionPerformed

