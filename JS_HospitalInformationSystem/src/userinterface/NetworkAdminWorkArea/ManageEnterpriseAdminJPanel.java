    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        
        Enterprise enterprise = (Enterprise) enterpriseJComboBox.getSelectedItem();
        
        String username = usernameJTextField.getText();
        String password = String.valueOf(passwordJPasswordField.getPassword());
        String name = nameJTextField.getText();
        boolean x=false,y=false,z=false,x2=true,y2=true,z2=true;
        for(Network nw:system.getNetworkList()){
            
            for(Enterprise e:nw.getEnterpriseDirectory().getEnterpriseList()){
                    
                    x=system.getUserAccountDirectory().checkIfUsernameIsUnique(username);
                    y=e.getUserAccountDirectory().checkIfUsernameIsUnique(username);
                    z=nw.getUserAccountDirectory().checkIfUsernameIsUnique(username);
                    if(x==false) x2=x;
                    if(y==false) y2=y;
                    if(z==false) z2=z;
                
            }
        }
        
        if(x2==true && y2==true && z2==true){
            People people= enterprise.getPeopleDirectory().createPeople(name);

            if(enterpriseTypeJTF.getText().equals("Hospitals")){
                UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, people, new HAdminRole());
            }else if(enterpriseTypeJTF.getText().equals("PharmacySupplier")){
                UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, people, new PSAdminRole());
            }else if(enterpriseTypeJTF.getText().equals("MedicalInsurance")){
                UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, people, new MIAdminRole());
            }
            populateTable();
            JOptionPane.showMessageDialog(null,"Create successfully");
            usernameJTextField.setText("");
            passwordJPasswordField.setText("");
            nameJTextField.setText("");
        }else{
            JOptionPane.showMessageDialog(null,"Username already existed");
        }
    }//GEN-LAST:event_submitJButtonActionPerformed

