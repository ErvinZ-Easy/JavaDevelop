    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()) {
                    Object[] row = new Object[3];
                    row[0] = userAccount;
                    row[1] = enterprise.getName();
                    row[2] = network.getName();
                    

                    model.addRow(row);
                }
            }
        
    }

    private void populateNetworkComboBox(){
        networkJComboBox.removeAllItems();
        networkJComboBox.addItem(network);
        
    }
    
    private void populateEnterpriseComboBox(){
        enterpriseJComboBox.removeAllItems();
        
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            enterpriseJComboBox.addItem(enterprise);
            
        }
        
    }
     private void populateEnterpriseType(Enterprise e){
        for (Network n:system.getNetworkList()){
            for(Enterprise ep:n.getEnterpriseDirectory().getEnterpriseList()){
                if(ep==e){
                    enterpriseTypeJTF.setText(e.getEnterpriseType().toString());
                }
            }
        }
     }
    
    
