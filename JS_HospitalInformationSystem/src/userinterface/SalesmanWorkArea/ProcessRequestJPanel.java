 public void populateTable(){
        
        DefaultTableModel dtm = (DefaultTableModel) purchaseJTable.getModel();
        dtm.setRowCount(0);
        Medicine m=request.getMedicine();
        Object[] row = new Object[4];
        row[0] = m;
        row[1] = m.getQuantity();
        row[2] = request;
        row[3] = m.getStatus();
        dtm.addRow(row);
            
    }
 
