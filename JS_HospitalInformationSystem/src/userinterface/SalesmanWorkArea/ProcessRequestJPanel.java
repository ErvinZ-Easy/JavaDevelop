    private void btnShipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShipActionPerformed

      //  request.setTestResult(resultJTextField.getText());
      try{
        int selectedrow=purchaseJTable.getSelectedRow();
        if(selectedrow>=0){
            Medicine medicine =(Medicine)purchaseJTable.getValueAt(selectedrow,0);
            medicine.setPrice(Double.parseDouble(price.getText()));
            if(!medicine.getStatus().equals("Completed")){
                giveStorage(medicine.getMedName(),medicine.getQuantity());
                JOptionPane.showMessageDialog(null,"succeed!");
                
            }else{
                JOptionPane.showMessageDialog(null,"Already shipped");
                return;
            }
            
            medicine.setStatus("Completed");
            request.setStatus("Completed");
            populateTable();
            
            
        }else{
            JOptionPane.showMessageDialog(null,"please choose row");
            return;
        }
      }catch(Exception e){
          JOptionPane.showMessageDialog(null,"please enter price");
          return;
      }
    }//GEN-LAST:event_btnShipActionPerformed
        
    public void giveStorage(String medName,int quantity){
        for(Network n:system.getNetworkList()){
            for(Enterprise e:n.getEnterpriseDirectory().getEnterpriseList()){
                for(Organization o:e.getOrganizationDirectory().getOrganizationList()){
                    for(UserAccount ua :o.getUserAccountDirectory().getUserAccountList()){
                        if(ua.getUsername().equals(request.getSender().getUsername())){
                            e.getStorageDir().addStorage(medName,quantity);
                            
                        }
                    }
                }
                
            }
        }
    }

